let x1 = 10; // number
x1 += 5; // number (x1 = x1 + 5)
console.log(x1); // Output: 15

x1 -=3; // number (x1 = x1 - 3)
console.log(x1); // Output: 12

x1 *= 2; // number (x1 = x1 * 2)
console.log(x1); // Output: 24  