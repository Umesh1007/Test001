// +,-,*,/

//Assignment operator

let a = 10;
let b = 5;

//Arithmetic Operators
let sum = a + b; // addition
let sub = a - b; // subtraction
let mul = a * b; // multiplication
let div = a / b;  // division

//output
console.log("Sum: " + sum); // Output: Sum: 15
console.log("Subtraction: " + sub); // Output: Subtraction: 5
console.log("Multiplication: " + mul);      // Output: Multiplication: 50
console.log("Division: " + div); // Output: Division: 2



//Modulus operator - it will give you remainder

console.log("Remainder: " + (a % b)); // Output: Remainder: 0

// n%2 == 1 --- Odd Number
// n%2 == 0 --- Even Number

//Exponentiation operator (**), power 2^3 = 8
 console.log (2**3); // Output: 8