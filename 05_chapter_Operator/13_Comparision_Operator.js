// =, ==, ===, !=, !==, >, <, >=, <=

//= -> Assignment operator, used to assign a value to a variable.

//== -> losse comparsion (Sikh vs Hindu)

//=== -> strict comparsion (Sikh vs hindu, language, culture, food, etc)


console.log(10 == 10); // Output: true
console.log(10 == '10'); // Output: true (loose comparison)
console.log(10 === '10'); // Output: false (strict comparison)
console.log(10 != 5); // Output: true
console.log(10 !== '10'); // Output: true (strict comparison)
console.log(10 > 5); // Output: true
console.log(10 < 5); // Output: false
console.log(10 >= 10);  // Output: true
console.log(10 <= 5); // Output: false              


